""" Calculate the total cost of buying multiple bags of popcorn """


# created a variable to store the cost of popcorn
popcorn_cost = 4.25

# created a variable to store the number of bags bought 
bags_purchased = 3

# multipled the popcorn_cost and the number of bags_purchased
total_cost = popcorn_cost * bags_purchased

# display total 
print(total_cost)